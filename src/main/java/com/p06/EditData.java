    public EditData(String id, HashMap<String, String> data, java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        this.ID = id;
        this.data = data;
        initComponents();
        
        try {
            JPanel panel = new JPanel();
            panel.setLayout(new GridLayout(0, 1, 0, 10));
            
            int index = 1;
            
            Input panel_input = new Input();
            panel_input.id.setText(String.valueOf(1));
            panel_input.kunci.setText("_id");
            panel_input.nilai.setText(ID);
            
            panel_input.kunci.setEnabled(false);
            panel_input.nilai.setEnabled(false);
            
            panel.add(panel_input);

            for (Map.Entry<String, String> item : data.entrySet()) {
                panel_input = new Input();
                panel_input.id.setText(String.valueOf(index+1));
                panel_input.kunci.setText(item.getKey());
                panel_input.nilai.setText(item.getValue());
                panel.add(panel_input);
                index++;
            }

            DaftarInput.setViewportView(panel);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }


    private void PerbaharuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PerbaharuiActionPerformed
        // TODO add your handling code here:
        try {
            MongoCollection<Document> col = mongodb.getCollection("koleksi");
            Document doc = new Document();
            
            JPanel panel = (JPanel) DaftarInput.getViewport().getView();
            
            for (Component component : panel.getComponents()) {
                Input panel_input = (Input) component;
                if (!"1".equals(panel_input.id.getText())) {
                    doc.put(panel_input.kunci.getText(), panel_input.nilai.getText());
                }
            }
            
            Document filter = new Document("_id", new ObjectId(ID));
            Document DataBaru = new Document("$set", doc);
            
            col.updateOne(filter, DataBaru);
            
            CRUDMongoDB.UpdateData();
            
            JOptionPane.showMessageDialog(this, "Berhasil Memperbaharui Data");
        } catch (Exception e) {
            System.out.println("Error : " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Gagal Memperbaharui Data");
        }
        
        this.dispose();
    }//GEN-LAST:event_PerbaharuiActionPerformed

